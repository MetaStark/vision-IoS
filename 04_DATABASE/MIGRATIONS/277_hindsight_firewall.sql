-- SECTION 9: Cohort-Aware Backfill Function
-- ============================================================================

CREATE OR REPLACE FUNCTION fhq_governance.populate_brier_decomposition_retrospective(
    p_start_date DATE DEFAULT NULL,
    p_end_date DATE DEFAULT NULL,
    p_asset_class TEXT DEFAULT NULL
)
RETURNS TABLE (
    inserted_count INTEGER,
    non_eligibility_logged INTEGER,
    cohorts_processed TEXT[]
) AS $$
DECLARE
    v_inserted INTEGER := 0;
    v_non_eligibility INTEGER := 0;
    v_cohorts TEXT[] := ARRAY[]::TEXT[];
    v_record RECORD;
    v_decomposition_id UUID;
BEGIN
    -- Process each decomposition with retrospective flag
    FOR v_record IN
        SELECT
            cmp.*,
            CASE
                WHEN cmp.asset_id LIKE 'BTC%' OR cmp.asset_id LIKE 'ETH%' THEN 'CRYPTO'
                WHEN cmp.asset_id IN ('SPY', 'QQQ', 'IWM') THEN 'EQUITY'
                WHEN cmp.asset_id IN ('GLD', 'TLT', 'UUP') THEN 'MACRO'
                ELSE 'OTHER'
            END AS asset_class_derived
        FROM fhq_governance.compute_brier_decomposition(
            p_start_date := p_start_date,
            p_end_date := p_end_date
        ) cmp
        WHERE (p_asset_class IS NULL OR
               CASE
                   WHEN cmp.asset_id LIKE 'BTC%' OR cmp.asset_id LIKE 'ETH%' THEN 'CRYPTO'
                   WHEN cmp.asset_id IN ('SPY', 'QQQ', 'IWM') THEN 'EQUITY'
                   WHEN cmp.asset_id IN ('GLD', 'TLT', 'UUP') THEN 'MACRO'
                   ELSE 'OTHER'
               END = p_asset_class)
    LOOP
        -- Insert with retrospective flag = TRUE
        INSERT INTO fhq_governance.brier_decomposition (
            asset_id,
            asset_class,
            forecast_type,
            horizon,
            regime,
            period_start,
            period_end,
            sample_size,
            brier_score,
            reliability,
            resolution,
            uncertainty,
            is_retrospective,
            retrospective_computed_at,
            evidence_hash
        ) VALUES (
            v_record.asset_id,
            v_record.asset_class_derived,
            v_record.forecast_type,
            'T+1',  -- Default horizon
            v_record.regime,
            v_record.period_start,
            v_record.period_end,
            v_record.sample_size,
            v_record.brier_score,
            v_record.reliability,
            v_record.resolution,
            v_record.uncertainty,
            TRUE,  -- CRITICAL: Retrospective flag
            NOW(),  -- When analysis was performed
            v_record.evidence_hash || ':RETROSPECTIVE'
        )
        RETURNING decomposition_id INTO v_decomposition_id;

        v_inserted := v_inserted + 1;

        -- Register for non-eligibility tracking
        PERFORM fhq_governance.register_retrospective_decomposition(v_decomposition_id);
        v_non_eligibility := v_non_eligibility + 1;

        -- Track cohorts
        IF NOT v_record.asset_class_derived = ANY(v_cohorts) THEN
            v_cohorts := v_cohorts || v_record.asset_class_derived;
        END IF;
    END LOOP;

    RETURN QUERY SELECT v_inserted, v_non_eligibility, v_cohorts;
END;
$$ LANGUAGE plpgsql;

COMMENT ON FUNCTION fhq_governance.populate_brier_decomposition_retrospective IS
'CEO-DIR-2026-105: Retrospective Brier decomposition backfill with Hindsight Firewall.
All data inserted is marked is_retrospective = TRUE and registered for Non-Eligibility tracking.
Includes cohort binding (asset_class) per Rule 1.';


