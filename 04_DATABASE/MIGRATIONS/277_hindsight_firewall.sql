CREATE OR REPLACE FUNCTION fhq_governance.check_hindsight_contamination()
RETURNS TABLE (
    check_name TEXT,
    is_contaminated BOOLEAN,
    contaminated_count INTEGER,
    details TEXT
) AS $$
BEGIN
    -- Check 1: ex_ante_regime_beliefs references
    RETURN QUERY
    SELECT
        'ex_ante_regime_beliefs'::TEXT,
        EXISTS(
            SELECT 1 FROM fhq_governance.ex_ante_regime_beliefs earb
            JOIN fhq_governance.brier_decomposition bd
                ON earb.created_at > bd.retrospective_computed_at
            WHERE bd.is_retrospective = TRUE
              AND earb.created_at > bd.computed_at
        )::BOOLEAN,
        0::INTEGER,
        'Checks if retrospective data influenced ex-ante beliefs'::TEXT;

    -- Check 2: SkillDamper direct references (via governance_actions_log)
    RETURN QUERY
    SELECT
        'skilldamper_actions'::TEXT,
        EXISTS(
            SELECT 1 FROM fhq_governance.governance_actions_log gal
            WHERE gal.action_type = 'SKILLDAMPER_ADJUSTMENT'
              AND gal.metadata->>'source' LIKE '%retrospective%'
        )::BOOLEAN,
        0::INTEGER,
        'Checks if retrospective data was used for SkillDamper'::TEXT;

    -- Check 3: decision_plan artifacts
    RETURN QUERY
    SELECT
        'decision_plans'::TEXT,
        FALSE::BOOLEAN,  -- No decision_plan table exists yet
        0::INTEGER,
        'No decision_plan artifacts exist - N/A'::TEXT;
END;
$$ LANGUAGE plpgsql STABLE;

