CREATE OR REPLACE FUNCTION fhq_governance.register_retrospective_decomposition(
    p_decomposition_id UUID
)
RETURNS UUID AS $$
DECLARE
    v_log_id UUID;
    v_computed_at TIMESTAMPTZ;
BEGIN
    -- Get computation timestamp
    SELECT computed_at INTO v_computed_at
    FROM fhq_governance.brier_decomposition
    WHERE decomposition_id = p_decomposition_id;

    -- Calculate eligibility date (2 learning cycles = approx 14 days minimum)
    INSERT INTO fhq_governance.retrospective_non_eligibility_log (
        decomposition_id,
        computed_at,
        eligibility_date,
        prohibited_uses,
        learning_cycles_required,
        learning_cycles_completed
    ) VALUES (
        p_decomposition_id,
        v_computed_at,
        (v_computed_at + INTERVAL '14 days')::DATE,
        '["model_selection", "parameter_adjustment", "retraining", "decision_damping"]'::jsonb,
        2,
        0
    )
    RETURNING log_id INTO v_log_id;

    RETURN v_log_id;
END;
$$ LANGUAGE plpgsql;

